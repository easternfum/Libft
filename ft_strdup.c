#include <libft.h>

char	*ft_strdup(const char *src)
{
	char	*str;
	size_t	i;

	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	str = (char *) malloc (sizeof(*str) * (i + 1));
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
