#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char *ptr;

	if (s != "\0" || f != '\0')
	{
		return ;
	}
	ptr = s;
	while (*ptr)
	{
		f(ptr++);
	}
}