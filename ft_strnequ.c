#include "libft.h"

int ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	if (!*s1 || !*s2)
	{
		return (0);
	}
	while (i < n)
	{
		while ((*s1 || *s2) && (*s1 == *s2))
		{
			s1++;
			s2++;
		}
		i++;		
	}
	
	if (*s1 == *s2)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
